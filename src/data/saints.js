// Funzione per ottenere il santo del giorno
export const getSaintOfTheDay = (date = new Date()) => {
  const month = String(date.getMonth() + 1).padStart(2, '0');
  const day = String(date.getDate()).padStart(2, '0');
  const key = `${month}-${day}`;
  
  console.log(`Cercando santo per data: ${key}`);
  console.log(`Santo trovato:`, saintsData[key]);
  
  return saintsData[key] || {
    name: "Santo del Giorno",
    description: "Informazioni non disponibili per oggi",
    feast_day: date.toLocaleDateString('it-IT')
  };
};

